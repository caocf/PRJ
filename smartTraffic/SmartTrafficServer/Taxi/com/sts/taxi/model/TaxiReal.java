package com.sts.taxi.model;

public class TaxiReal {
	String sbid;
	
	int jd;
	int wd;

	
	double distance;


	public String getSbid() {
		return sbid;
	}


	public void setSbid(String sbid) {
		this.sbid = sbid;
	}


	public int getJd() {
		return jd;
	}


	public void setJd(int jd) {
		this.jd = jd;
	}


	public int getWd() {
		return wd;
	}


	public void setWd(int wd) {
		this.wd = wd;
	}


	public double getDistance() {
		return distance;
	}


	public void setDistance(double distance) {
		this.distance = distance;
	}
	
	
}
