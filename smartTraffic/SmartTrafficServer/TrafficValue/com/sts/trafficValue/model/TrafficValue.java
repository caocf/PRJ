package com.sts.trafficValue.model;

public class TrafficValue 
{
	private int areaTpiNum;
	private String areaName;
	private double tpi;
	private String tpiLevel;
	private double tpiOld;
	private double avgSpeed;
	private long time;
	private int travelTime;
	private double length;
	private double coverage;
	private double hrRate;
	public int getAreaTpiNum() {
		return areaTpiNum;
	}
	public void setAreaTpiNum(int areaTpiNum) {
		this.areaTpiNum = areaTpiNum;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public double getTpi() {
		return tpi;
	}
	public void setTpi(double tpi) {
		this.tpi = tpi;
	}
	public String getTpiLevel() {
		return tpiLevel;
	}
	public void setTpiLevel(String tpiLevel) {
		this.tpiLevel = tpiLevel;
	}
	public double getTpiOld() {
		return tpiOld;
	}
	public void setTpiOld(double tpiOld) {
		this.tpiOld = tpiOld;
	}
	public double getAvgSpeed() {
		return avgSpeed;
	}
	public void setAvgSpeed(double avgSpeed) {
		this.avgSpeed = avgSpeed;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public int getTravelTime() {
		return travelTime;
	}
	public void setTravelTime(int travelTime) {
		this.travelTime = travelTime;
	}
	public double getLength() {
		return length;
	}
	public void setLength(double length) {
		this.length = length;
	}
	public double getCoverage() {
		return coverage;
	}
	public void setCoverage(double coverage) {
		this.coverage = coverage;
	}
	public double getHrRate() {
		return hrRate;
	}
	public void setHrRate(double hrRate) {
		this.hrRate = hrRate;
	}
	
	
	
	
}
