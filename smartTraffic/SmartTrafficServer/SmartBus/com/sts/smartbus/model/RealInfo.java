package com.sts.smartbus.model;


public class RealInfo {
	int num;
	double distance;
	int interStationNum;
	int crowed;
	int speed;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public double getDistance() {
		return distance;
	}
	public void setDistance(double distance) {
		this.distance = distance;
	}
	public int getInterStationNum() {
		return interStationNum;
	}
	public void setInterStationNum(int interStationNum) {
		this.interStationNum = interStationNum;
	}
	public int getCrowed() {
		return crowed;
	}
	public void setCrowed(int crowed) {
		this.crowed = crowed;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
}
